package com.school.management.model;

/**
 * Enum representing different types of fees charged by the school.
 */
public enum FeeType {
    /**
     * Base tuition fee charged per month
     */
    TUITION,

    /**
     * Library usage fee
     */
    LIBRARY,

    /**
     * Computer lab fee
     */
    COMPUTER,

    /**
     * Sports and physical education fee
     */
    SPORTS,

    /**
     * Special one-time fee (admission, first month)
     */
    SPECIAL,

    /**
     * Examination fee
     */
    EXAMINATION,

    /**
     * Annual maintenance fee
     */
    MAINTENANCE,

    /**
     * Transport/Bus fee
     */
    TRANSPORT
}
